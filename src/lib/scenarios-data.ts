export interface StepOptions {
  /** 步骤 3 给情绪命名：4 选 1 + 其他 */
  emotions: string[];
  /** 步骤 4 我想要：4 选 1 + 其他 */
  wants: string[];
  /** 步骤 5 我愿意：4 选 1 + 其他 */
  wills: string[];
  /** 步骤 5 我不愿意：4 选 1 + 其他 */
  wonts: string[];
  /** 步骤 6 开场白：3 选 1 + 我要换一句 */
  openers: string[];
  /** 步骤 7 演练：基于 step 6 选的 opener 给 2-3 个"说出口"变体 + 我要换 */
  user_says_variants: string[];
  /** 步骤 8 对方反驳：1 句（演示） */
  counter: string;
  /** 步骤 8 应对：3 选 1 */
  counter_replies: string[];

  // ============ v2.0.7.7 (ADR-005 P0 + 双路径演练) ============
  // 习惯模式（暴露用户的讨好/回避/压抑模式），让用户先选"平时会怎么做"
  /** 步骤 3 习惯情绪（"没事" / "没感觉" / "还行"） */
  habit_emotions?: string[];
  /** 步骤 4 习惯想要（"我想让大家都不为难"） */
  habit_wants?: string[];
  /** 步骤 5 习惯愿意（"我愿意帮忙"） */
  habit_wills?: string[];
  /** 步骤 5 习惯不愿意（"我不愿意让 ta 失望"） */
  habit_wonts?: string[];
  /** 步骤 6 习惯开场白（"没事我可以"） */
  habit_openers?: string[];
  /** 步骤 7 AI 反映话术（按 persona 选一条显示） */
  reflections?: {
    wen?: string[];
    zhi?: string[];
    song?: string[];
  };
}

